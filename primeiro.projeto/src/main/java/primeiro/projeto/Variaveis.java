package primeiro.projeto;

public class Variaveis {
	
	public static void main(String[] args) {
		
		int numeroInteiro = 1;
		double numeroQuebrado = 1.5;
		boolean verdadeFalso = true;
		
		System.out.println("Variável do tipo inteiro pode receber "  + numeroInteiro);
		System.out.println("Variável do tipo quebrado pode receber "  + numeroQuebrado);
		System.out.println("Variável do tipo falso pode receber "  + verdadeFalso);
	}

}
